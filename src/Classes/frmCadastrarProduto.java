    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed
        if(p.getManufatura().size() > 0){
            if(p.getIngredientes().size() > 0){
                if(!"".equals(txtNomeProduto.getText())){
                    p.setNome(txtNomeProduto.getText());
                    p.setCusto(Float.parseFloat(txtCustoProduto.getText()));
                    pd.getProdutos().add(p);
                    pd.setTotalPedido(pd.calculaPedido());
                    frmped.calculaCusto();
                    this.dispose();
                }else{
                    infoBox("Nome do produto vazio!", "Campos em branco");
                }
            }else{
                infoBox("Ingredientes não cadastrados!", "Campos em branco");
            }
        }else{
            infoBox("Manufatura não cadastrada!", "Campos em branco");
        }
    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed
