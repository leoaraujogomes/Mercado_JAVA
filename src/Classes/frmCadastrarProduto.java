    private void btnCadastrarManufaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarManufaturaActionPerformed
        if (!"".equals(txtProcessoManufatura.getText()) && !"".equals(txtQtdeHoras.getText())){
            if(mnft.getProcesso().getOperador() != null){
                if(mnft.getProcesso().getEquipamento() != null){
        mnft.getProcesso().setNome(txtProcessoManufatura.getText());
        p.getManufatura().add(mnft);
        txtCustoHoraManufatura.setText(String.valueOf(mnft.getProcesso().calculaCusto()));
        mnft.setQtde(Float.parseFloat(txtQtdeHoras.getText()));
        btnOperador.setEnabled(true);
        btnEquipamento.setEnabled(true);
        mnft = new Manufatura();
        txtCustoProduto.setText(String.valueOf(p.calculaCusto()));
                }else{
                    infoBox("Equipamento vazio!", "Campos em branco");
                }
            }else{
                infoBox("Operador vazio!", "Campos em branco");
            }
        }else{
            infoBox("Há campos em branco, preencha-os!", "Campos em branco");
        }
    }//GEN-LAST:event_btnCadastrarManufaturaActionPerformed
    public MateriaPrima formataMateria(){
       MateriaPrima pr = new MateriaPrima();
       pr.setNome(txtNomeIngrediente.getText());
       pr.setFornecedor(txtFornecedorIngrediente.getText());
       pr.setPreco(Float.parseFloat(txtPrecoIngrediente.getText()));
       pr.setUnidade(txtUnidadeIngrediente.getText());
       return pr;
    }
    private void btnCadastrarIngredienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarIngredienteActionPerformed
       if (!"".equals(txtNomeIngrediente.getText()) && !"".equals(txtFornecedorIngrediente.getText()) && !"".equals(txtPrecoIngrediente.getText()) && !"".equals(txtUnidadeIngrediente.getText()) && !"".equals(txtQtdeIngrediente.getText())){
       ingr.setMateriaPrima(formataMateria());
       ingr.setQtde(Float.parseFloat(txtQtdeIngrediente.getText()));
       p.getIngredientes().add(ingr);
       ingr = new Ingrediente();
       txtCustoProduto.setText(String.valueOf(p.calculaCusto()));
       }else{
           infoBox("Há campos em branco, preencha-os!", "Campos em branco");
       }
    }//GEN-LAST:event_btnCadastrarIngredienteActionPerformed

