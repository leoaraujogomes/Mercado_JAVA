    public frmListarPedido(ArrayList<Pedido> pedd) {
        initComponents();
        listaPedidos = pedd;
        DefaultTableModel modelo = (DefaultTableModel)TablePedidos.getModel();
        for (int i = 0; i < listaPedidos.size(); i++){
        modelo.addRow(new String[] {String.valueOf(listaPedidos.get(i).getNumero()),
            listaPedidos.get(i).getCliente().getNome(),
            String.valueOf(listaPedidos.get(i).getData().getDia())
            +"/"+String.valueOf(listaPedidos.get(i).getData().getMes())
            +"/"+String.valueOf(listaPedidos.get(i).getData().getAno()),
            String.valueOf(listaPedidos.get(i).getTotalPedido())});
        }
    }
